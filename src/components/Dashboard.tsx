const defaultTheme = createTheme();

export default function Dashboard({cartBadgeNumber, gamesInCart, handleDelete, changeApiLink}) {
  const [open, setOpen] = React.useState(true);
  const toggleDrawer = () => {
    setOpen(!open);
  };

  const [inputVal, setInputVal] = useState("");

  const handleInputChange = (e) => {
    setInputVal(e.target.value);
  };

  const handleSubmit = (e) => {
    e.preventDefault();
    //send the api link with the search tag
    changeApiLink(`https://api.rawg.io/api/games?key=e6ddcb78aeda4b678f21e8f6a97890cf&search=${inputVal}&page_size=50`)

    return redirect("login");

  };

  return (
    <ThemeProvider theme={defaultTheme}>
      <Box sx={{ display: 'flex' }}>
        <CssBaseline />
        <AppBar position="absolute" open={open}>
          <Toolbar
            className='toolBar'
            sx={{
              pr: '24px', // keep right padding when drawer closed
            }}
          >
            <IconButton
              edge="start"
              color="inherit"
              aria-label="open drawer"
              onClick={toggleDrawer}
              sx={{
                marginRight: '36px',
                ...(open && { display: 'none' }),
              }}
            >
              <MenuIcon />
            </IconButton>

            <Typography
              component="h1"
              variant="h6"
              color="inherit"
              noWrap
              sx={{ flexGrow: 1 }}
            >
              Vapor: The Ultimate Game Store
            </Typography>
            <div className="searchBarContainer">
              <form className="d-flex" role="search" onSubmit={handleSubmit}>
                <input className="form-control me-2" type="search" placeholder="Search for a game!" aria-label="Search" 
                  value={inputVal}
                  onChange={handleInputChange}
                />
                <button className="btn btn-outline-success" type="submit">Search</button>
              </form>
            </div>
            <IconButton color="inherit">
              <Badge badgeContent={cartBadgeNumber} color="secondary">
                <OffCanvas 
                gamesInCart={gamesInCart}
                handleDelete={handleDelete}
                />
              </Badge>  
            </IconButton>
          </Toolbar>
        </AppBar>
        <Drawer variant="permanent" open={open}>
          <Toolbar
            sx={{
              display: 'flex',
              alignItems: 'center',
              justifyContent: 'flex-end',
              px: [1],
            }}
          >
            <IconButton onClick={toggleDrawer}>
              <ChevronLeftIcon />
            </IconButton>
          </Toolbar>
          <Divider />
          <List component="nav">
            {mainListItems}
            <Divider sx={{ my: 1 }} />
            {secondaryListItems}
          </List>
        </Drawer>


        <Outlet /> 

      </Box>
    </ThemeProvider>
  );
}
